def ascii_to_icon(ascii_art, color_mapping, cell_size=1, output_path=None):
    """
    Convert ASCII art to a PNG icon where each character represents a specific color.

    Parameters:
    - ascii_art (list): 2D array/list of strings representing the ASCII art
    - color_mapping (dict): Maps ASCII characters to RGB color tuples
    - cell_size (int): Size of each cell in pixels
    - output_path (str): Path to save the resulting PNG file

    Returns:
    - PIL.Image: The generated image
    """
    import numpy as np
    from PIL import Image

    height = len(ascii_art)
    width = max(len(row) for row in ascii_art)

    # Create a blank image
    img_array = np.zeros((height * cell_size, width * cell_size, 3), dtype=np.uint8)

    # Fill in the colors based on the ASCII art
    for y, row in enumerate(ascii_art):
        for x, char in enumerate(row):
            if char in color_mapping:
                y_start, y_end = y * cell_size, (y + 1) * cell_size
                x_start, x_end = x * cell_size, (x + 1) * cell_size
                img_array[y_start:y_end, x_start:x_end] = color_mapping.get(char, (255, 255, 255))

    # Convert to PIL Image
    img = Image.fromarray(img_array)

    # Save if path provided
    if output_path:
        img.save(output_path)

    return img


